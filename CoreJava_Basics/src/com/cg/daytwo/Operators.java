package com.cg.daytwo;

public class Operators {

	public static void main(String[] args) {
		int a = 10;
		int b = 10;
		a++;             //unary
		a += 5;          //asignment
		System.out.println(a);
		System.out.println(a+b);   //arithmetic
        System.out.println(a>b && b>a);   //logical
	    System.out.println(a>>b);  //shift
	
	}

}
