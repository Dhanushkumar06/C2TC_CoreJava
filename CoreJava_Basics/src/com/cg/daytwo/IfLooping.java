package com.cg.daytwo;

public class IfLooping {

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 12;
		if(num1>num2) {
			System.out.println("Number1 is Greater");
		}
		else if(num1==num2) {
			System.out.println("Both are Equal");
		}
		else {
			System.out.println("Number2 is Greater");
		}

	}

}
