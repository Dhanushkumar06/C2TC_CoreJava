package com.cg.dayone;

public class Basics {

	public static void main(String[] args) {
		int a = 20, b = 10;

		  int sum = a + b;
		  System.out.println("Sum is: " + sum);
		  
		  int dif = a - b;
		  System.out.println("Diff is : " + dif);

		  int mul = a * b;
		  System.out.println("Multiple is : " + mul);

		  //Division Operation
		  int div = a / b;
		  System.out.println("Div is : " + div);

	}

}
